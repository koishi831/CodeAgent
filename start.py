#!/usr/bin/env python3
"""
CodeAgent - 基于大模型的智能编程助手
========================================

项目整体架构：
┌─────────────────────────────────────────────────────────────┐
│                        start.py                               │
│                    (程序入口文件)                              │
└──────────────────────┬──────────────────────────────────────┘
                       │
                       ▼
┌─────────────────────────────────────────────────────────────┐
│                  __main__.py                                  │
│         (主程序入口：加载环境、启动REPL)                        │
└──────────────────────┬──────────────────────────────────────┘
                       │
           ┌───────────┴───────────┐
           │                       │
           ▼                       ▼
┌──────────────────┐    ┌──────────────────┐
│    agent.py      │    │     ui.py        │
│  (核心Agent类)    │    │  (终端UI模块)    │
│  - 对话管理       │    │  - 用户交互      │
│  - 工具调用       │    │  - 输出展示      │
│  - 上下文管理     │    │  - 费用统计      │
│  - 子Agent调用    │    └──────────────────┘
└────────┬─────────┘
         │
    ┌────┴────┬─────────┬──────────┐
    │         │         │          │
    ▼         ▼         ▼          ▼
┌─────────┐ ┌───────┐ ┌───────┐ ┌──────────┐
│ tool.py │ │memory │ │prompt │ │subagent │
│(工具系统)│ │(记忆) │ │(提示)  │ │(子Agent) │
└─────────┘ └───────┘ └───────┘ └──────────┘

核心功能模块说明：

1. agent.py - 核心逻辑模块
   - Agent 类：主代理，管理对话、工具调用、上下文
   - 工具调用处理：执行各种工具并处理结果
   - 子 Agent 调用：启动子代理处理特定任务
   - 上下文管理：裁剪和压缩对话历史以节省 token

2. tool.py - 工具系统
   - 工具定义：read_file, write_file, edit_file, list_files, grep_search, run_shell, web_fetch, agent
   - 权限检查：危险操作需要用户确认
   - 工具执行：统一的工具调用入口

3. memory.py - 记忆系统
   - 记忆文件管理：创建、解析记忆文件
   - 记忆索引：自动更新记忆索引
   - 记忆提示：为 Agent 提供记忆信息

4. prompt.py - 系统提示词
   - 构建系统提示：包含工作目录、日期、平台、记忆、子Agent描述等
   - 模板替换：动态生成适合当前环境的提示词

5. subagent.py - 子 Agent 系统
   - 子 Agent 类型：explore(只读检索)、plan(规划)、general(通用任务)
   - 系统提示：为不同类型子 Agent 提供专门的提示词

6. ui.py - 用户界面
   - 终端输出：使用 Rich 库美化输出
   - 费用统计：计算并显示 token 消耗
   - 用户交互：危险操作确认、命令输入等

7. __main__.py - 主程序
   - 环境加载：从 .env 文件加载配置
   - REPL 循环：交互式对话界面
   - 命令处理：/exit, /clear, /cost, /compact, /memory

使用流程：
1. 配置 .env 文件（OPENAI_MODEL_ID, OPENAI_BASE_URL, OPENAI_API_KEY）
2. 运行 python start.py
3. 输入自然语言指令与 Agent 交互
4. 使用 / 开头的特殊命令进行控制
"""

